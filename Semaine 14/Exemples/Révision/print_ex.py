# print() affiche à la console
print("Bonjour")

# Si on spécifie plusieurs arguments, ils sont affichés séparés par un espace par défaut
print("Bonjour", "les", 1)

# on peut changer le séparateur
print("Bonjour", "les", 1, sep="****")

# print ajoute une nouvelle ligne, \n, à la fin par défaut.  On peut le changer.
print("Bonjour", end="!!!!!!")
print("Salut", end="->")


