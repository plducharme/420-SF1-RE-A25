# ceci est équivalent
print(42e3)
print(42E3)

# On peut utiliser les opérateurs unaires
print(-33E4)
print(66e-6)

# Multiplier deux entiers donne un entier
print(42*10)  # donne 420
# Multiplier en utilisant un nombre avec une partie fractionnaire donnera un résultat avec une partie fractionnaire
print(3.7 * 72)

# La notation scientifique est utilisée pour les nombres >= 10e15
print(3e16)
print(3e4)

